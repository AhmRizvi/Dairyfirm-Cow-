    private void createaccountbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createaccountbtnActionPerformed
        Pattern email = Pattern.compile("^[a-z0-9._]+\\@[a-z0-9]+\\.[a-z]{2,5}$");
        Pattern passpatt = Pattern.compile("^[a-z0-9]{8,20}$");
        if (namefeild.getText().equals("Name")) {
            namelabel.setText("Enter your Name");
            namelabel.setForeground(Color.red);
        } else {
            namelabel.setText(null);
            namelabel.setForeground(null);
        }
        if (emailfeild.getText().equals("example_.06@gmail.com") || !email.matcher(emailfeild.getText()).matches()) {
            emaillabel.setText("Enter your email");
            emaillabel.setForeground(Color.red);
        } else {
            emaillabel.setText(null);
            emaillabel.setForeground(null);
        }
        if (setpassword.getText().equals("Password")) {
            passlabel1.setText("Enter your password");
            passlabel1.setForeground(Color.red);
        } else {
            passlabel1.setText(null);
            passlabel1.setForeground(null);
        }

        if (passpatt.matcher(setpassword.getText()).matches()) {
            passlabel1.setText(null);
            passlabel1.setForeground(null);
        } else {
            passlabel1.setText("password will be 8-20 digit");
            passlabel1.setForeground(Color.red);
        }

        if (setpassword.getText().equals(confirmpassword.getText())) {
            passmatchlabel.setText(null);
            passmatchlabel.setForeground(null);
        } else {
            passmatchlabel.setText("Doesnot Match Password");
            passmatchlabel.setForeground(Color.red);
        }
        if (codeforcreate.getText().equals("code")) {
            codeforcreate.setForeground(Color.red);
            codelabel.setText("wrong code");
            codelabel.setForeground(Color.red);
        } else {
            codelabel.setText(null);
            codelabel.setForeground(null);
        }
        if (codeforcreate.getText().equals("1205")) {
            codeforcreate.setForeground(Color.green);
        } else {

        }
        if (email.matcher(emailfeild.getText()).matches() && codeforcreate.getText().equals("1205") && passpatt.matcher(setpassword.getText()).matches() && confirmpassword.getText().equals(setpassword.getText())
                && !emailfeild.getText().equals("example_.06@gmail.com") && !namefeild.getText().equals("Name") && !setpassword.getText().equals("Password")) {
            insertuserdata();
            JOptionPane.showMessageDialog(null, "insert data");
        }


    }//GEN-LAST:event_createaccountbtnActionPerformed

