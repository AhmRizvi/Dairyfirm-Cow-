    public void insertuserdata() {
        try {

            try {
                linkwithdatabase = DriverManager.getConnection("jdbc:mysql://localhost:3306/cow_firm_project", "root", "nh12345");
                if (linkwithdatabase != null) {
                    System.out.println("Connected");
                }
                prestat = linkwithdatabase.prepareStatement("insert into user_table values(?,?,?,?)");
                prestat.setString(1, namefeild.getText());//name
                prestat.setString(2, emailfeild.getText());//email
                prestat.setString(3, setpassword.getText());
                prestat.setInt(4, Integer.parseInt(codeforcreate.getText()));

                int n = prestat.executeUpdate();
                if (n > 0) {
                    System.out.println("Insert done");
                } else {
                    System.out.println("Data doesnot insert");
                }

            } catch (java.sql.SQLIntegrityConstraintViolationException e) {
                JOptionPane.showMessageDialog(this, "Duplicate entry!");

            }

        } catch (Exception e) {

        } finally {
            try {
                linkwithdatabase.close();
                prestat.close();
            } catch (Exception e) {
            }
        }
    }

